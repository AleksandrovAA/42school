#include <stdlib.h>
#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
    int i;
    
    i = -1;
    if (!f || !s)
        return ;
	while (s[++i])
		f(i, &s[i]);
}
